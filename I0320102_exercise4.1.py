#Operator Aritmetika

x = 15
y = 4

#Penjumlahan
print('x + y = ', x+y)

#Pengurangan
print('x - y = ', x-y)

#Perkalian
print('x * y = ', x * y)

#Pembagian
print('x / y = ', x/y)

#Pembagian dengan pembulatan ke bawah
print('x // y = ', x//y)

#Pangkat
print('x ** y = ', x**y)